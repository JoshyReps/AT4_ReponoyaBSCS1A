    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        //this gets the text pf the main text field
        String userID = jTextField2.getText();
        
        //this basically prevents the user from removing all the accounts in the
        //json file becuase that will prevent usage of our login disabling us from adding
        //account which is not good
        if(users.size() == 1) {
            
            jLabel1.setText("Required to Keep One");
        }
        
        //this basically checks if the text field is not blank when we press the button
        if(!userID.isBlank()) {
            
            //we first use the custom built helper class `findID` which will do as
            //the name suggest, find the ID, whether it exist in the `users` or not.
            JSONObject user = HelperClass.findID(userID);
            
            //if the user was not found users, then we basically ignore the code block
            //and if so, then it will run what's inside.
            if(user != null) {
                
                //this is basically a built in class which is part of the GUI which
                //will show a small interface that will just prompt a message. After that,
                //then we basically remove the `user` if it is found, then we save the file,
                //and lastly we go back. (The return is meant to escape the method call)
                JOptionPane.showMessageDialog(this, "Successfully Deleted " + (String) user.get("lastName"));
                users.remove(user);
                HelperClass.saveFile(users);
                back();
                return;
            }
        }
        
        //if this is ever reached, that means, that the `user` was not found (user implies null)
        //then we basically promp another interface which shows the message, and then it goes back to the menu
        JOptionPane.showMessageDialog(this, "No Account was Deleted");
        back();
    }//GEN-LAST:event_jButton1ActionPerformed

