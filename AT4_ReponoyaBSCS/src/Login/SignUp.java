    //this method is run when we hit the `create account` method.
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        //As we can see. The first retrieve all the text input that 
        //was hopefully placed in each of the three text fields.
        String firstName = jTextField1.getText();
        String lastName = jTextField2.getText();
        String department = jTextField3.getText();
        
        //now, this conditional statement will check if all three of the text
        //fields actually has an input.
        if(firstName.isBlank() || lastName.isBlank() || department.isBlank()){
            
            //Now,if atleast one of the text field is blank then we basically change 
            //the main label to promp input missing.
            jLabel1.setText("SOME INPUT MISSING!");
        }
        else {
            
            //however, if each text field DOES have an input, then we basically,
            //exit the SignUp object and move in the usernamePassword object, which
            //will have the final process or procedure in creating a new account.
            this.setVisible(false);
            new UsernamePassword(firstName, lastName, department, this);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

